package test.main;
/*
 * 	[ Java 참조 데이터 type ]
 * 
 * 	String
 * 
 *		- 문자열을 다룰때 사용하는 데이터 type이다.
 *
 * */
public class MainClass05 {
	public static void main(String[] args) {
		System.out.println("메인 메소드가 시작되었습니다.");
		
		int num = 1;
		
		// String type 데이터를 만들어서 변수에 대입, 문자열의 id값(참조값)이 들어감
		String name = "김구라"; 
		
		// 변수를 선언만 하면 만들어지지 않는다.
		String str;
		
		// 값을 대입해야 만들어진다.
		str = "문자열";
		
		System.out.println("메인 메소드가 종료됩니다.");
		
		//객체는 field와 method로 이루어짐
		
		
	}
}
